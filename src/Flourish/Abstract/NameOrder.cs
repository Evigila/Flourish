namespace ArkheideSystem.Flourish.Abstract;

/// <summary>
/// Specifies how profile name parts are displayed.
/// </summary>
public enum NameOrder
{
    /// <summary>
    /// Displays the first name before the last name.
    /// </summary>
    FirstLast,

    /// <summary>
    /// Displays the last name before the first name.
    /// </summary>
    LastFirst,
}
